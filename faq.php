                    para proteger el acero de la corrosión. El recubrimiento protector se produce al sumergir productos
                    de acero en un baño de zinc fundido. La película de zinc que se forma sobre el acero lo protege de
                    dos maneras, protección de barrera y protección galvánico (catódica). Es este último tipo de
                    protección la que permite que productos de acero puedan permanecer sin corrosión durante décadas.
                    Esto se explica porque en presencia de humedad el zinc actúa como ánodo y el acero como cátodo, de
                    manera que el zinc se corroe en una acción de sacrificio y evita que el acero se oxide. Los equipos
                    GREENMATIK tienen una larga vida útil en cualquier clima porqué son galvanizados en caliente con
